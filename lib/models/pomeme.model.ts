import mongoose from "mongoose";

const pomemeSchema = new mongoose.Schema({
  text: { type: String, required: true },
  author: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  community: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "Community",
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  parentId: {
    type: String,
  },
  children: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Pomeme",
    },
  ],
});

const Pomeme = mongoose.models.Pomeme || mongoose.model("Pomeme", pomemeSchema);

export default Pomeme;
